    private void btnEgaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEgaleActionPerformed

        if (plus == true) {
            c = a + b;
            plus = false;
        } else if (sur == true) {
            if (b == 0) {
                c=0;
            } else if (b > 0) {
                c = a / b;
            }
            sur = false;
        } else if (moins == true) {
            c = a - b;
            moins = false;
        } else if (fois == true) {
            c = a * b;
            fois = false;
        }

        if(c == 0){
            txtAfficheur.setText("Peux pas diviser par zero");
        }
        else if (c == (int) c) {
            txtAfficheur.setText(String.valueOf((int) c));
        } else {
            txtAfficheur.setText(String.valueOf(c));
        }

        checkOperation = false;
        num = 0;
        egale = true;

    }//GEN-LAST:event_btnEgaleActionPerformed

